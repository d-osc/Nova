function main(): number {
    // Array.prototype.toString()
    // Converts array to comma-separated string representation
    // Example: [10, 20, 30] -> "10,20,30"

    let arr = [10, 20, 30];
    let str = arr.toString();

    // str = "10,20,30"
    // str.length = 8

    // Test: str.length = 8
    return str.length;
}
