// Test Math.sqrt() function
function main(): number {
    // Math.sqrt() returns square root
    // For integer type system, returns integer approximation
    let a = Math.sqrt(144);     // 12
    let b = Math.sqrt(25);      // 5
    let c = Math.sqrt(100);     // 10

    // Result: 12 + 5 + 10 = 27
    return a + b + c;
}
