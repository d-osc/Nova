// Test basic modulo operations
function main(): number {
    // Basic modulo tests
    let a = 10 % 3;   // 1
    let b = 15 % 4;   // 3
    let c = 20 % 5;   // 0
    let d = 7 % 2;    // 1 (odd number)
    let e = 8 % 2;    // 0 (even number)

    // Sum: 1 + 3 + 0 + 1 + 0 = 5
    return a + b + c + d + e;
}
